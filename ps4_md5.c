void ps4_check_md5(PS4Nor* obj)
{
	const unsigned char* buffer = obj->buffer;
	unsigned char md5[36];
	int found;
	int r;

	//
	ps4_md5(md5, buffer + s0_emc_ipl_a_offset, s0_emc_ipl_a_len);

	found = -1;

	for (int i = 0; i < ARRAY_SIZE(EMC_IPL_MD5); ++i)
	{
		if (0 == strcmp(md5, EMC_IPL_MD5[i]))
		{
			found = i;
			break;
		}
	}

	r = found > -1 ? VA_OK : VA_ERROR;
	ps4_out(obj, WHITE, r, "%08X length=%08X segment 0: emc_ipl_a  MD5=%s", s0_emc_ipl_a_offset, s0_emc_ipl_a_len, md5);

	//
	ps4_md5(md5, buffer + s0_emc_ipl_b_offset, s0_emc_ipl_b_len);

	found = -1;

	for (int i = 0; i < ARRAY_SIZE(EMC_IPL_MD5); ++i)
	{
		if (0 == strcmp(md5, EMC_IPL_MD5[i]))
		{
			found = i;
			break;
		}
	}

	r = found > -1 ? VA_OK : VA_ERROR;
	ps4_out(obj, WHITE, r, "%08X length=%08X segment 0: emc_ipl_b  MD5=%s", s0_emc_ipl_b_offset, s0_emc_ipl_b_len, md5);

	//
	ps4_md5(md5, buffer + s0_eap_kbl_offset, s0_eap_kbl_len);

	for (int i = 0; i < ARRAY_SIZE(EAP_KBL_MD5); ++i)
	{
		if (0 == strcmp(md5, EAP_KBL_MD5[i]))
		{
			found = i;
			break;
		}
	}

	r = found > -1 ? VA_OK : VA_ERROR;
	ps4_out(obj, WHITE,r , "%08X length=%08X segment 0: eap_kbl    MD5=%s", s0_eap_kbl_offset, s0_eap_kbl_len, md5);
	
	//
	ps4_md5(md5, buffer + s0_wifi_offset, s0_wifi_len);

	found = -1;

	for (int i = 0; i < ARRAY_SIZE(TORUS_MD5); ++i)
	{
		if (0 == strcmp(md5, TORUS_MD5[i]))
		{
			found = i;
			break;
		}
	}

	r = found > -1 ? VA_OK : VA_ERROR;
	ps4_out(obj, WHITE, r, "%08X length=%08X segment 0: wifibt     MD5=%s", s0_wifi_offset, s0_wifi_len, md5);
}

